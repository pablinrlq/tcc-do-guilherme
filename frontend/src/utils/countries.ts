export const COUNTRIES: CountryOption[] = [...RAW].sort((a, b) =>
  a.label.localeCompare(b.label, 'pt-BR'),
);

const BY_CODE = new Map(COUNTRIES.map((c) => [c.value, c.label]));
const BY_LABEL = new Map(COUNTRIES.map((c) => [c.label.toLowerCase(), c]));

export function countryLabel(code: string): string {
  return BY_CODE.get(code) ?? code;
}

/** Converte código ISO ou rótulo legado para o código padrão (vazio se desconhecido). */
export function normalizeCountry(input: string): string {
  if (!input) return '';
  if (BY_CODE.has(input)) return input;
  const found = BY_LABEL.get(input.toLowerCase());
  return found ? found.value : '';
}
